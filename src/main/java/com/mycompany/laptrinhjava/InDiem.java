    private void nutXoaDiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_nutXoaDiemActionPerformed
       if(maMonHoc != null){
       Component frame = null;
       int result = JOptionPane.showConfirmDialog(frame,
                        "Bạn có chắc muốn xóa điểm này của sinh viên " + Quanlidiem1.mssvInDiem,
                        "Xác nhận",
                        JOptionPane.YES_NO_OPTION,
                        JOptionPane.QUESTION_MESSAGE);
                if(result == JOptionPane.YES_OPTION){
                    SinhVienDAO.xoaDiem(Quanlidiem1.mssvInDiem, maMonHoc);
                    hienThiBangDiem();
                    maMonHoc = null ;
                }else if (result == JOptionPane.NO_OPTION){
                    hienThiBangDiem();
                    maMonHoc = null ;
                }
       }else{
                    JFrame frame = new JFrame("JOptionPane showMessageDialog example");
                    JOptionPane.showMessageDialog(frame,
                    "Vui lòng chọn môn học cần xóa",
                    "Không thể sửa điểm",
                    JOptionPane.ERROR_MESSAGE);
                }
    }//GEN-LAST:event_nutXoaDiemActionPerformed

