    private void hienThiBangDiem() {
        
        String namHoc = chonNamHoc.getSelectedItem().toString();
        String hocKi = chonHocKi.getSelectedItem().toString();
        float tong = 0;
        float diemTB = 0 ;
        bangDiem=SinhVienDAO.layBangDiemTheoMSSV(Quanlidiem1.mssvInDiem,namHoc,hocKi);
        
        tableModel.setRowCount(0);
        
        bangDiem.forEach((ketqua) -> {
           tableModel.addRow(new Object[] {ketqua.getMaMH(), ketqua.getTenMH(),ketqua.getDiem(),ketqua.getDiemChu()
                });
       });
        
        for (BangDiem i : bangDiem) {
             if(i.getDiemChu().equals("A")){
                 tong += 4 ;
             }else if(i.getDiemChu().equals("B+")){
                 tong += 3.5 ;
             }else if(i.getDiemChu().equals("B")){
                 tong += 3 ;
             }else if(i.getDiemChu().equals("C+")){
                 tong += 2.5 ;
             }else if(i.getDiemChu().equals("C")){
                 tong += 2 ;
             }else if(i.getDiemChu().equals("D")){
                 tong += 1.5 ;
             }else if(i.getDiemChu().equals("F")){
                 tong += 0 ;
             }
        }
        
        
        diemTB = (float) (Math.round(tong/bangDiem.size()*100)/100D);
        String diemTBString  = String.valueOf(diemTB);
        if(diemTBString.equals("0.0")){
            diemTb.setText("");
        }else{
            diemTb.setText(diemTBString);
        }
   }
    
    

